require('dotenv').config();

const notificationService = require('../services/notification.service');

async function run() {
  const donorEmail = 'akhilkrishnakondri07@gmail.com';

  if (!process.env.EMAIL_USER || !process.env.EMAIL_PASSWORD) {
    console.error('Missing EMAIL_USER or EMAIL_PASSWORD in backend/.env');
    process.exit(1);
  }

  const subject = 'Test: Matched Donor Notification - LifeLink';
  const html = `
    <div style="font-family: Arial, sans-serif; max-width: 620px; margin: 0 auto;">
      <h2 style="color: #dc3545;">Test Matched Donor Alert</h2>
      <p>Hello Donor,</p>
      <p>This is a test email to verify the matched-donor notification channel.</p>
      <div style="background: #f8f9fa; padding: 14px; border-radius: 6px; margin: 16px 0;">
        <p><strong>Blood Group:</strong> O+</p>
        <p><strong>Urgency:</strong> URGENT</p>
        <p><strong>Hospital:</strong> City General Hospital</p>
        <p><strong>Units Required:</strong> 2</p>
      </div>
      <p>
        <a href="${(process.env.FRONTEND_URL || 'http://localhost:3000').replace(/\/$/, '')}/donor-dashboard.html"
           style="background: #28a745; color: white; padding: 10px 16px; text-decoration: none; border-radius: 4px;">
          Open Donor Dashboard
        </a>
      </p>
      <p style="font-size: 12px; color: #666; margin-top: 22px;">Generated by test-matched-donor-email.js</p>
    </div>
  `;

  console.log(`Sending test matched-donor email to: ${donorEmail}`);
  console.log(`From sender configured as: ${process.env.EMAIL_USER}`);

  const result = await notificationService.sendEmailNotification(donorEmail, subject, html);

  if (!result.success) {
    console.error('Email test failed:', result.error);
    process.exit(1);
  }

  console.log('Email test sent successfully.');
  console.log(`Message ID: ${result.messageId || 'N/A'}`);
}

run().catch((error) => {
  console.error('Unexpected test script error:', error.message);
  process.exit(1);
});
